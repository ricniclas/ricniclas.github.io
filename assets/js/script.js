async function applyTranslations(lang) {

  // if (!translations[lang]) {
  //   console.log("loading tranlations");
  //   translations[lang] = await loadTranslations(lang);
  // }
  
  if(lang == "en"){
    translations[lang] = language_en
  }
  else{
    translations[lang] = language_pt;
  }

  console.log("loaded tranlations");
  const langData = translations[lang];
  for (const [id, text] of Object.entries(langData)) {
      const element = document.getElementById(id);
      if (element) {
          element.innerHTML = text;
      }
  }

}

async function init() {
    const savedLang = localStorage.getItem('app-language') || 'en';
    document.getElementById('language-select').value = savedLang;
    await applyTranslations(savedLang);
    
    // Handle language change
    document.getElementById('language-select').addEventListener('change', async (e) => {
        const lang = e.target.value;
        localStorage.setItem('app-language', lang);
        await applyTranslations(lang);
    });
}



